//double point class 
//used for film height and width as doubles
public class DoublePoint {
	private double x;
	private double y;
	
	public DoublePoint(double x, double y){
		this.x = x;
		this.y = y;
	}
	
	public double getX(){
		return x;
	}
	
	public double getY(){
		return y;
	}

}
